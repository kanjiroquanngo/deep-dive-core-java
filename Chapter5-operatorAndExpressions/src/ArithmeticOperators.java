import java.lang.*;
import java.util.*;
public class ArithmeticOperators {
    public static void main(String[] args) {
        float a = 14.3f, b = 3.2f;
        float c = (float)a / b;
        float r = a % b;
        
        System.out.println(c);
        System.out.println(r);
        System.out.println((10+20)/2);
    }
}
